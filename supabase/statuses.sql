-- ============================================================================
-- Protoboard — свои статусы у каждого проекта (выполнить ОДИН раз, ДО публикации)
--
-- SQL Editor → New query → вставить → Run. Делает:
--   1) добавляет у проектов колонку statuses (список колонок доски);
--   2) проставляет существующим проектам три базовых статуса;
--   3) снимает старое ограничение «статус только todo/check/done»
--      (теперь статусы у каждого проекта свои).
-- Безопасно запускать повторно.
-- ============================================================================

-- 1. Колонка со списком статусов проекта (id, название, цвет, порядок = порядок в массиве).
alter table public.projects add column if not exists statuses jsonb;

-- 2. Существующим проектам — три базовых статуса (id совпадают со старыми значениями,
--    поэтому уже существующие задачи останутся в своих колонках).
update public.projects
   set statuses = '[
     {"id":"todo","label":"To Do","color":"#7C8499"},
     {"id":"check","label":"Ready to Check","color":"#D67E1E"},
     {"id":"done","label":"Done","color":"#16A06A"}
   ]'::jsonb
 where statuses is null;

-- 3. Снимаем старое ограничение на значения tasks.status (теперь id статусов произвольные).
alter table public.tasks drop constraint if exists tasks_status_check;
