import { createContext, useContext } from "react";

// Простая локализация RU/EN. Русский — язык по умолчанию и одновременно ключ словаря:
// компоненты вызывают t("Русская строка"), а на "en" она подменяется переводом из EN.
// Если перевода нет — возвращается исходная русская строка (не ломается молча).
export const LangContext = createContext("ru");

export function useLang() {
  return useContext(LangContext);
}

export function useT() {
  const lang = useContext(LangContext);
  return (str) => (lang === "en" ? (EN[str] ?? str) : str);
}

